import ts from 'typescript';
import path from 'path';
import fs from 'fs';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '../..');

export function generateDockviewCoreExports() {
    const packageDir = path.join(ROOT, 'packages/dockview-core');
    const configPath = path.join(packageDir, 'tsconfig.json');

    const configFile = ts.readConfigFile(configPath, ts.sys.readFile);
    const parsed = ts.parseJsonConfigFileContent(
        configFile.config,
        ts.sys,
        packageDir
    );

    const program = ts.createProgram(parsed.fileNames, parsed.options);
    const checker = program.getTypeChecker();

    const entryFile = path.join(packageDir, 'src/index.ts');
    const sourceFile = program.getSourceFile(entryFile);

    if (!sourceFile) {
        throw new Error(`Could not find source file: ${entryFile}`);
    }

    const symbol = checker.getSymbolAtLocation(sourceFile);

    if (!symbol) {
        throw new Error(`Could not get module symbol for: ${entryFile}`);
    }

    const exports = checker.getExportsOfModule(symbol);
    const names = exports.map((e) => e.getName()).sort();

    const header = [
        '# Auto-generated by scripts/gen/dockview-core-exports.mjs',
        '# Do not edit manually. Run `yarn gen` to regenerate.',
        '',
    ].join('\n');

    const output = header + names.join('\n') + '\n';
    const outputPath = path.join(ROOT, '__generated__/dockview-core-exports.txt');

    fs.writeFileSync(outputPath, output, 'utf-8');

    console.log(
        `  Wrote ${names.length} exports to __generated__/dockview-core-exports.txt`
    );
}
